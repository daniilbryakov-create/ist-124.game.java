package com.aimtrainer;

/**
 * Перечисление типов игровых режимов.
 * Каждый тип содержит уникальную игровую механику и параметры.
 * Параметры каждого режима определены в соответствующем классе-сцене.
 */
public enum GameType {

    /**
     * Режим "Движущийся шарик".
     * Нужно навести прицел на движущийся шарик и удерживать ЛКМ около 1 секунды для
     * уничтожения.
     * Цель: набрать 20 очков.
     */
    MOVING_HOLD,

    /**
     * Режим "Исчезающий шарик".
     * Статичный шарик появляется на случайное место и исчезает через 2 секунды.
     * Нужно кликнуть ЛКМ до исчезновения.
     * Цель: собрать 30 шариков, пока они видны.
     */
    DISAPPEARING,

    /**
     * Режим "Падающие шарики".
     * Шарики падают сверху. Зелёные нужно ловить, фиолетовые нужно избегать.
     * Управление: корзинка следует за мышью горизонтально.
     * Цель: не допустить 3 ошибки (потеря зелёного или поимка фиолетового).
     */
    FALLING_CATCH
}
